# What is List? How will you reverse a list?
"""
A list is a built-in data structure that allows you to store a collection of items in a single variable. Lists are ordered, mutable ,
 and can contain items of different types. You can create a list using square brackets, with items separated by commas.
"""
#  For example:
my_list = [1, 2, 3, 4, 5]
reversed_list=my_list[::-1]
print(reversed_list)