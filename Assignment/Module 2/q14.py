# What are negative indexes and why are they used?
"""
Python is a powerful and versatile programming language that has many features and capabilities. One of these features is negative
 indexing, which allows you to access elements of a sequence (such as a list, a string, or a tuple) from the end, using negative 
 numbers as indexes.
"""
my_list=[1,2,3,4,5]
print(my_list[-1])
print(my_list[::-1])